    private void ButtonResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonResultActionPerformed

        num2 = Double.parseDouble(numbers);

        switch (operator) {
            case 1:
                numbers = String.valueOf(num1 + num2);
                jTextPane1.setText(numbers);
                break;
            case 2:
                numbers = String.valueOf(num1 - num2);
                jTextPane1.setText(numbers);
                break;
            case 3:
                numbers = String.valueOf(num1 * num2);
                jTextPane1.setText(numbers);
                break;
            case 4:
                numbers = String.valueOf(num1 / num2);
                jTextPane1.setText(numbers);
                break;
            case 5:
                numbers = String.valueOf(java.lang.Math.pow(num1, num2));
                jTextPane1.setText(numbers);
                break;
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_ButtonResultActionPerformed

